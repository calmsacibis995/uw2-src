#define TTY_SIZE	63		/* maximum path length for a tty */
#define NAME_SIZE	63		/* maximum length for a login name */

typedef struct msg {
	char	m_type;
	char	m_unused;
	short	m_pid;
	char	m_tty[TTY_SIZE+1];
        union {
                char    name[NAME_SIZE+1];
                struct  sockaddr_in remoteaddr; /* remote host address */
        } m_addr;
#define m_name          m_addr.name
#define m_remote        m_addr.remoteaddr
} msg_t;

/* m_type's */
#define MDIAL   18
#define	MSTAT	17
#define	MTTY	16
#define	MPID	15
